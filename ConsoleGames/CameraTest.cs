        }
        public struct Player
        {
            private Point2I pos;
            public Point2I Pos { get => pos; set => pos = value; }
            public Player(Point2I pos)
            {
                Pos = pos;
            }
            public void Move(Level level, Dir dir)
            {
                Point2I newPos = Pos + dir;
                if (!(level[newPos] == 1))
                {
                    Pos += dir;
                }
            }
            public void Draw(Level level)
            {
                Point2I cameraPos = (level.Camera.X, level.Camera.Y);
                MyConsole.Draw(Pos - cameraPos, "^^", ConsoleColor.Green);
            }
        }
